package Assessment_June21;

public class VarDeclarationInitialization {
    public static void main(String[] args) {
        int a=20;
        double b=30.5;
        boolean c=true;
        char d='d';
        System.out.println("a:"+a);
        System.out.println("b:"+b);
        System.out.println("c:"+c);
        System.out.println("d:"+d);

    }
}
